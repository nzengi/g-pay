//! Deterministic test vectors for cross-language validation.
//! When this test passes in Rust, the same (input, output) tuples must be reproduced
//! by any other-language port (e.g. the TypeScript implementation in apps/api-gateway).

use stealth_core::{derive_stealth_address_deterministic, SpendKey, ViewKey};

const SPEND_PRIV: [u8; 32] = [
    0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10,
    0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x00,
];

const VIEW_PRIV: [u8; 32] = [
    0xa0, 0xa1, 0xa2, 0xa3, 0xa4, 0xa5, 0xa6, 0xa7, 0xa8, 0xa9, 0xaa, 0xab, 0xac, 0xad, 0xae, 0xaf,
    0xb0, 0xb1, 0xb2, 0xb3, 0xb4, 0xb5, 0xb6, 0xb7, 0xb8, 0xb9, 0xba, 0xbb, 0xbc, 0xbd, 0xbe, 0x00,
];

const R_SEED: [u8; 64] = [
    0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x2a, 0x2b, 0x2c, 0x2d, 0x2e, 0x2f, 0x30,
    0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39, 0x3a, 0x3b, 0x3c, 0x3d, 0x3e, 0x3f, 0x40,
    0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47, 0x48, 0x49, 0x4a, 0x4b, 0x4c, 0x4d, 0x4e, 0x4f, 0x50,
    0x51, 0x52, 0x53, 0x54, 0x55, 0x56, 0x57, 0x58, 0x59, 0x5a, 0x5b, 0x5c, 0x5d, 0x5e, 0x5f, 0x60,
];

const EXPECTED_SPEND_PUB: &str = "616e237719716e25ead63d831f9117f79b5aa05af8be30ff0eddb3dc43e8bdcf";
const EXPECTED_VIEW_PUB: &str = "3e97bbe3dad77cdbab3b9d7a5af963868b2ee668470874b566dad4a32076c98b";
const EXPECTED_STEALTH_PUB: &str =
    "20fa85036bcc5661f62af10c241ee8243e2543735e7e869c58df13b02f3c26c3";
const EXPECTED_EPHEMERAL_R: &str =
    "21c24081dfbed643c24ca431092386e1cb0830937d5b4f4cc0d6f366586338b0";
const EXPECTED_VIEW_TAG: u8 = 0xbf;

#[test]
fn canonical_vector_matches() {
    let spend = SpendKey::from_bytes(&SPEND_PRIV).expect("valid spend scalar");
    let view = ViewKey::from_bytes(&VIEW_PRIV).expect("valid view scalar");
    let nonce: u64 = 7;

    let spend_pub = spend.public();
    let view_pub = view.public();
    let addr = derive_stealth_address_deterministic(&spend_pub, &view_pub, &R_SEED, nonce);

    assert_eq!(hex::encode(spend_pub.to_bytes()), EXPECTED_SPEND_PUB);
    assert_eq!(hex::encode(view_pub.to_bytes()), EXPECTED_VIEW_PUB);
    assert_eq!(hex::encode(addr.stealth_pubkey), EXPECTED_STEALTH_PUB);
    assert_eq!(hex::encode(addr.ephemeral_r), EXPECTED_EPHEMERAL_R);
    assert_eq!(addr.view_tag, EXPECTED_VIEW_TAG);
}
